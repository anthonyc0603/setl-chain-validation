package io.setl.websocket.messages.serializers;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;
import java.io.IOException;

public class RequestSerializer extends JsonSerializer<Object> {
  @Override
  public void serialize(Object value, JsonGenerator generator, SerializerProvider serializers) throws IOException {
    generator.writeStartObject();

    if (value instanceof String) {
      generator.writeFieldName("RequestName");
      generator.writeString(value.toString());
    }

    generator.writeEndObject();
  }
}
